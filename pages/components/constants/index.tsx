export function applyDiscount(price: string, discount: string) {
  const priceNumber = parseFloat(price.replace('R$ ', '').replace(',', '.'));
  const discountNumber = parseFloat(discount.replace('%', ''));
  const newValue = priceNumber * (1 - discountNumber / 100);
  return `R$ ${newValue.toLocaleString('pt-BR', { minimumFractionDigits: 2, maximumFractionDigits: 2 })}`;
}

